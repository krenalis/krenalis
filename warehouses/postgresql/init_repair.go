// CanInitialize checks whether the data warehouse can be initialized.
func (warehouse *PostgreSQL) CanInitialize(ctx context.Context) error {
	pool, err := warehouse.connectionPool(ctx)
	if err != nil {
		return err
	}
	const query = `SELECT
		"c"."relname",
		CASE
			"c"."relkind"
			WHEN 'r' THEN 'table'
			WHEN 'm' THEN 'materialized view'
			WHEN 'i' THEN 'index'
			WHEN 'S' THEN 'sequence'
			WHEN 'v' THEN 'view'
			WHEN 'c' THEN 'type'
			ELSE "c"."relkind"::text
		END
	FROM
		"pg_class" "c"
		JOIN "pg_namespace" "n" ON "n"."oid" = "c"."relnamespace"
	WHERE
		"n"."nspname" = $1
		AND "n"."nspname" NOT LIKE 'pg_toast%'
	ORDER BY
		"c"."relname"`
	rows, err := pool.Query(ctx, query, warehouse.settings.Schema)
	if err != nil {
		return err
	}
	defer rows.Close()
	var objects []string
	for rows.Next() {
		var name, typ string
		err := rows.Scan(&name, &typ)
		if err != nil {
			return err
		}
		objects = append(objects, fmt.Sprintf("%s '%s'", typ, name))
	}
	if err := rows.Err(); err != nil {
		return err
	}
	if objects != nil {
		reason := fmt.Sprintf("expected an empty database, got: %s", strings.Join(objects, ", "))
		return meergo.NewWarehouseNonInitializableError(reason)
	}
	return nil
}

// Initialize initializes the database objects on the data warehouse in order to
// make it work with Meergo.
func (warehouse *PostgreSQL) Initialize(ctx context.Context, userColumns []meergo.Column) error {
	return warehouse.initRepair(ctx, userColumns, false)
}

// Repair repairs the database objects on the data warehouse needed by Meergo.
func (warehouse *PostgreSQL) Repair(ctx context.Context, userColumns []meergo.Column) error {
	return warehouse.initRepair(ctx, userColumns, true)
}

// initRepair initializes (or repairs) the database objects (as tables, types,
// etc...) on the data warehouse.
func (warehouse *PostgreSQL) initRepair(ctx context.Context, userColumns []meergo.Column, repair bool) error {
	pool, err := warehouse.connectionPool(ctx)
	if err != nil {
		return err
	}
	queries := []string{
		createDestinationUsersTable,
		createEventsTable,
		createOperationsTable,
		userIdentitiesSQLSchema(userColumns),
		usersSQLSchema("_users_0", userColumns),
	}
	if !repair {
		// Since the "CREATE VIEW IF NOT EXISTS" statement does not exist in
		// PostgreSQL, the view is recreated only if initializing, not when
		// repairing, otherwise a "cannot drop columns from view" error is
		// returned by PostgreSQL in cases where the users table has different
		// columns than the default one.
		//
		// TODO(Gianluca): clarify this.
		queries = append(queries, usersViewSQLSchema(userColumns, "_users_0"))
	}
	for _, query := range queries {
		_, err := pool.Exec(ctx, query)
		if err != nil {
			return err
		}
	}
	return nil
}

// userIdentitiesSQLSchema returns the SQL schema (in the form "CREATE TABLE
// ...") of the user identities table, which includes, in addition to the
// columns used internally by the driver, the given user columns.
func userIdentitiesSQLSchema(userColumns []meergo.Column) string {
	var b strings.Builder
	b.WriteString(`CREATE TABLE IF NOT EXISTS _user_identities (
		"__pk__" integer GENERATED BY DEFAULT AS IDENTITY,
		"__action__" integer NOT NULL,
		"__is_anonymous__" boolean NOT NULL DEFAULT FALSE,
		"__identity_id__" text NOT NULL,
		"__connection__" integer NOT NULL,
		"__anonymous_ids__" text [],
		"__last_change_time__" timestamp without time zone NOT NULL,
		"__execution__" integer,
		"__gid__" uuid,
		"__cluster__" integer GENERATED BY DEFAULT AS IDENTITY`)
	for _, c := range userColumns {
		b.WriteString(",\n")
		b.WriteString(quoteIdent(c.Name))
		b.WriteByte(' ')
		b.WriteString(typeToPostgresType(c.Type))
	}
	b.WriteString(",\n" + `PRIMARY KEY ("__pk__"))`)
	return b.String()
}

// usersSQLSchema returns the SQL schema (in the form "CREATE TABLE ...") of the
// users table with the given name, which includes, in addition to the columns
// used internally by the driver, the given user columns.
func usersSQLSchema(name string, userColumns []meergo.Column) string {
	var b strings.Builder
	b.WriteString(`CREATE TABLE IF NOT EXISTS `)
	b.WriteString(quoteIdent(name))
	b.WriteString(` (
		"__id__" uuid,
		"__identities__" int [],
		"__last_change_time__" timestamp without time zone NOT NULL`)
	for _, c := range userColumns {
		b.WriteString(",\n")
		b.WriteString(quoteIdent(c.Name))
		b.WriteByte(' ')
		b.WriteString(typeToPostgresType(c.Type))
	}
	b.WriteByte(')')
	return b.String()
}

// usersViewSQLSchema returns the SQL schema (in the form "CREATE ... VIEW ...")
// of the users view which is based on the users table with the given name.
func usersViewSQLSchema(userColumns []meergo.Column, fromUsersTable string) string {
	var b strings.Builder
	b.WriteString(`CREATE OR REPLACE VIEW "users" AS
		SELECT
			"__id__",
			"__last_change_time__"`)
	for _, c := range userColumns {
		b.WriteString(",\n")
		b.WriteString(quoteIdent(c.Name))
	}
	b.WriteString(` FROM `)
	b.WriteString(quoteIdent(fromUsersTable))
	return b.String()
}
